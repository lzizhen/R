/*查询操作*/
/*单表查询*/

/*查询所有学生的信息*/
SELECT * 
FROM Student;

/*查询所有女生的女性*/
SELECT SNAME
FROM Student
WHERE SEX = '女';

/*查询成绩在80～89分之间的所有学生的选课记录，查询结果按照成绩的降序排列。*/
SELECT * 
FROM SC
WHERE GRADE >= 80 AND GRADE <= 89
ORDER BY GRADE DESC;

/*查询各个系的学生人数*/
SELECT DEPTNO, count(SNO)
FROM Student
GROUP BY DEPTNO;

/*连接查询*/
/*查询信息系年龄在21岁以下的女生的姓名及年龄*/
SELECT SNAME, AGE
FROM Student, Dept
WHERE Student.DEPTNO = Dept.DEPTNO
	AND Dept.DNAME = '信息'
	AND AGE <= 21
	AND SEX = '女'；

/*嵌套查询*/
/*查询选修课总学分在10学分以下的学生的姓名*/
SELECT SNAME
FROM Student
WHERE SNO IN
	(SELECT SNO
		FROM SC, Course
		WHERE SC.SNO = Course.CNO
		GROUP BY SNO
		HAVING SUM(CREDIT) <10);




/*查询各门课程的最高成绩的学生的姓名及其成绩*/

SELECT CNO, SNAME, GRADE
FROM Student, SC, SCX
WHERE SCX.CNO = SCX.SNO AND SCX.GRADE IN 
	(SELECT MAX(GRADE)
		FROM SC SCY
		WHERE SCX.CNO = SCY.CNO
		GROUP BY CNO);

/*查询选修了1001号学生所选修的全部课程的学生的学号*/
